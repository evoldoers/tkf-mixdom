def get_profiles(name):
    """Get site class profiles by name.

    Args:
        name: one of 'sjolander9', 'C10', 'C20'

    Returns:
        profiles: (D, 20) frequency profiles in alphabetical AA order
        weights: (D,) mixture weights
        names: list of component descriptions
    """
    if name == 'sjolander9':
        return sjolander9()
    elif name in ('C10', 'c10', 'lg_c10'):
        return le_gascuel_c10()
    elif name in ('C20', 'c20', 'lg_c20'):
        return le_gascuel_c20()
    else:
        raise ValueError(f"Unknown profile set: {name}. "
                         f"Choose from: sjolander9, C10, C20")
