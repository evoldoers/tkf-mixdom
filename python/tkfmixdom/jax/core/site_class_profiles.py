"""Fixed site class profiles for dynamic site classes.

Provides amino acid frequency profiles from published sources:
- Sjolander et al. 1996 / HMMER3: 9 components (Dirichlet mixture)
- Le-Gascuel C10: 10 components (Le, Lartillot & Gascuel 2008)
- Le-Gascuel C20: 20 components (Le, Lartillot & Gascuel 2008)

All profiles use the standard 20 amino acid alphabet:
  A C D E F G H I K L M N P Q R S T V W Y
(alphabetical by 1-letter code, as used throughout tkfmixdom)
"""
import numpy as np

# Amino acid order used internally (alphabetical)
_AA_ALPHA = "ACDEFGHIKLMNPQRSTVWY"

# HMMER/IQ-TREE order: A R N D C Q E G H I L K M F P S T W Y V
_AA_HMMER = "ARNDCQEGHILKMFPSTWYV"

# Mapping from HMMER order to alphabetical
_HMMER_TO_ALPHA = [_AA_HMMER.index(aa) for aa in _AA_ALPHA]


def _reorder_profiles(profiles_hmmer_order):
    """Reorder profiles from HMMER/IQ-TREE AA order to alphabetical."""
    return profiles_hmmer_order[:, _HMMER_TO_ALPHA]


